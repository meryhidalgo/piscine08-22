#include <unistd.h>

void	ft_complete_2_3(char *array, char *str)
{
	int	i;

	i = 0;
	while (str[i++] != 0 && i < 4)
	{
		if (str[i] == '2' && str[i + 4] == '3')
			array[i + 4] = '4';
	}
	while (str[i++] != 0 && i < 8 && i > 3)
	{
		if (str[i] == '2' && str[i - 4] == '3')
			array[i + 4] = '4';
	}
	while (str[i++] != 0 && i < 12 && i > 7)
	{
		if (str[i] == '2' && str[i + 4] == '3')
			array[(i - 8) * 4 + 1] = '4';
	}
	while (str[i++] != 0 && i < 16 && i > 11)
	{
		if (str[i] == '2' && str[i - 4] == '3')
			array[(i - 11) * 4 - 2] = '4';
	}
}
