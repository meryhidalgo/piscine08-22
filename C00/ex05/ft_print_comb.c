#include <unistd.h>

void	ft_print_comb(void)
{
	char	n[3];

	n [0] = 48;
	while (n[0] <= 55)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 56)
		{
			n[2] = n[1] + 1;
			while (n[2] <= 57)
			{
				write(1, n, 3);
				if (n[0] < 55)
				{
					write(1, ", ", 2);
				}
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
